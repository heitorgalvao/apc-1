#include <stdio.h>

int main() {
    // ++ incremento de 1
    // -- decremento de 1

    int numero = 10;
    printf("pre-incremento = %i\n", ++numero);
    printf("pre-decrescimento = %i\n", --numero);
    printf("pos-incrementro = %i\n", numero++);
    printf(" pos-decrescimento = %i\n", numero--);

return 0;
}